#include "libft.h"

t_list	*ft_lstnew(char *content)
{
	t_list	*new;

	new = malloc(sizeof(t_list));
	if (!new)
		return (0);
	new->next = NULL;
	new->content = ft_strdup(content);
	new->type = 0;
	return (new);
}
